import UIKit

class Beer{
    var imagem: String!
    var nome: String!
    var tagline: String!
    var teorAlcool: Float!
    var escalaAmargor: String!
    var desc: String!

    init(imagem: String, nome: String, tagline: String, teorAlcool: Float, escalaAmargor: String, desc: String){

        self.imagem = imagem
        self.nome = nome
        self.tagline = tagline
        self.teorAlcool = teorAlcool
        self.escalaAmargor = escalaAmargor
        self.desc = desc
        
    }
		

}
